    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        Clipboard systemClipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
        if (systemClipboard.isDataFlavorAvailable(DataFlavor.stringFlavor))
        {
            try {
                TEXT =  (String)systemClipboard.getData(DataFlavor.stringFlavor);
            } catch (UnsupportedFlavorException | IOException ex) {
                Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTextArea1.setText("");
            jTextArea1.setText(TEXT);
        }else{
            TEXT = null;
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!jTextField1.getText().isEmpty() && !jTextField1.getText().isEmpty()) {
            this.sender = new SenderTCP(jTextField1.getText(),Integer.parseInt(jTextField3.getText()), this);
            sender.connect();
            sender.send("TEXT", jTextArea1.getText());
            sender.close();
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(jButton5.getText().equals("disconnect")){
            receptor.disableReception();
            jLabel3.setText("port:-----");
            jButton5.setText("connect");
        }else{
            String port = jTextField2.getText();
            if(!port.isEmpty()){
                try {
                    receptor = new ReceptorTCP(Integer.parseInt(port), this);
                    jLabel3.setText("port: " + port);
                    receptor.enableReception();
                    jButton5.setText("disconnect");
                } catch (UnknownHostException ex) {
                    Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

    
